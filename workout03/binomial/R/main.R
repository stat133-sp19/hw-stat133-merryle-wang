#' @title bin_mean
#' @description checks input then calcs mean
#' @param trials, total number of trial
#' @param prob, chance of success
#' @return mean if input are good else error
#' @examples bin_mean(5,0.5)
#' @export
bin_mean <- function(trials,prob){
  if(check_trials(trials) && check_prob(prob)){
    return(aux_mean(trials,prob))
  }
}


